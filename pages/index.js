import Head from "next/head";
import Link from "next/link";
import styled from "styled-components";
import lottie from "lottie-web";
import { useEffect, useRef } from "react";

export default function Home() {
  const container = useRef(null);

  useEffect(() => {
    const animation = lottie.loadAnimation({
      container: container.current, // the dom element that will contain the animation
      renderer: "svg",
      loop: true,
      autoplay: true,
      repeat: false,
      path: "/tennis-ball.json", // the path to the animation json
    });
    return () => animation.destroy(); // to prevent rendering of a second animation
  }, []);

  return (
    <div>
      <Head>
        <title>Project Courtisoul</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <MainWrapper>
        <Styledh1>Welcome to Courtisoul</Styledh1>
        <div ref={container}></div>
        <Link href="/gamelist" passHref>
          <Styledh2>Click here to enter...</Styledh2>
        </Link>
      </MainWrapper>
    </div>
  );
}

const MainWrapper = styled.main`
  position: relative;
  text-align: center;
  display: flex;
  flex-direction: column;
  justify-content: center;
  align-items: center;
  gap: 2em;
  height: 100vh;
`;

const Styledh1 = styled.h1`
  position: absolute;
  top: 10%;
  z-index: 200;
  color: var(--text-primary);
`;

const Styledh2 = styled.h2`
  position: absolute;
  bottom: 20%;
  z-index: 200;
  color: var(--text-primary);

  :hover {
    color: var(--attention-color-primary);
    cursor: pointer;
  }
`;
